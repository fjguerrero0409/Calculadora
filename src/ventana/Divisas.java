       private void CalcularCambio(){
        
       cadenaNumeros =etiquetaCambio.getText();
       dinero = Double.parseDouble(cadenaNumeros);
       cambioDivisa();
       dinero *= cambio;
       etiquetaCambio2.setText(String.format("%.2f",dinero));
    }

    private void cambioDivisa(){
        if(divisa1.equals(divisa2)){
            cambio = 1;
            
        }
        else if(divisa1.equals("Dolares")&& divisa2.equals("Euros")){
            cambio=0.85;
        }
        else if(divisa1.equals("Dolares")&& divisa2.equals("Pesos Colombianos")){
            cambio =3.842;
        }
        
        else if(divisa1.equals("Euros")&& divisa2.equals("Dolares")){
            cambio=1.18 ;
        }
        
        else if(divisa1.equals("Euros")&& divisa2.equals("Pesos Colombianos")){
            cambio=4.524;
        }
        
        else if(divisa1.equals("Pesos Colombianos")&& divisa2.equals("Dolares")){
            cambio=0.00026;
        }
        
        else if(divisa1.equals("Pesos Colombianos")&& divisa2.equals("Euros")){
            cambio=0.00022;
            }
    }
 
